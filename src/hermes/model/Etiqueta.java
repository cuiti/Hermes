package hermes.model;


public class Etiqueta {
	private int id;
	private String texto;
	
	public Etiqueta(int id, String texto) {
		super();
		this.id = id;
		this.texto = texto;
	}

	/**
	 * Este constructor se usa para dar de alta una nueva etiqueta, el id aun no existe así que queda null
	 * @author Alfonso
	 * @param texto el nombre de la etiqueta
	 */
	public Etiqueta(String texto) {
		this.texto = texto;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTexto() {
		return texto;
	}

	public void setTexto(String texto) {
		this.texto = texto;
	}
	
	public String toString(){
		return texto;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + id;
		result = prime * result + ((texto == null) ? 0 : texto.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Etiqueta other = (Etiqueta) obj;
		if (id != other.id)
			return false;
		if (texto == null) {
			if (other.texto != null)
				return false;
		} else if (!texto.equals(other.texto))
			return false;
		return true;
	}
	
}
